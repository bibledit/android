std::string gethostname()
{
#ifdef CONFIG_WIN32
    /* WSAStartup(...) must be called before any Winsock func */
    return std::string();
#else
    char buf[64];
    if(::gethostname(buf, 64) < 0)
        return std::string();
    else
        return std::string(buf);
#endif
}

